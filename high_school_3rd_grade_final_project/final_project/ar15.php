				<h2>Co to je AR-15?</h2>
				<p>M16/M4 je standardní bojová puška U.S. ozbrojených sil a federálních, státních a lokálních právních složek. Ve většině případů jsou díly pro M16/M4 vyměnitelné s AR-15. To znamená, že v případě vážné události, jako například přírodní pohroma, budou díly, zásobníky a munice k nalezení téměř všude.</p>
				<h2>Historie</h2>
				<p>Po mnoho let si většina lidí myslela, že AR-15 je poloautomatická verze M-16. V roce 1959, Armalite prodal svoje práva na AR-15 zbrojovce Colt. Název AR byl ponechán. Mnoho lidí si myslí,že AR znamená Assault Rifle, toto označení není pravdivé. AR znamená Armalite Rifle.</p>
				<p>Puška AR-15 byla poprvé nasazena do služby v roce 1962 U.S. letectvem pro bezpečnostní policii. AR-15 dostala designaci M16. Armáda Spojených státu začala pušku testovat v roce 1965 pod označením XM16E1.</p>
				<p>První M16 měly mnoho problému, převážně selhání vyhodit vystřelenou nábojnici, která způsobila zaseknutí celé zbraně.</p>
				<p>Problém byl ve střelném prachu, který byl v nábojích používán a ještě některé další problémy se spolehlivostí. Tyto problémy byly opraveny a M-16A1 byla na světě. Variantu A1 následovala varianta A2, ze které byla později vyvinuta karabina M4. Karabina M4 měla tří rannou dávku, kdežto M4A1 byla plně automatická.</p>
				<h2>Vnitřní mechanismus</h2>
				<p>AR-15 se skládá ze tří hlavních komponent: Upper receiveru, závěru a nosiče závěru a lower receiveru.</p>
				<p>Upper a lower receiver jsou drženy u sebe dvěma kolíky. Vytlačením zadního kolíku se umožní nahnutí upper receiveru pro polní čištění a vytlačením obou kolíků se oddělí upper a lower receiver.</p>
				<p>Puška AR-15 má dvě varianty fungování. Jedna z nich je fungování za pomoci plynového pístu, tento způsob se moc nepoužívá. Více častější je přímý odběr plynů.</p>
				<p>Přímý odběr plynů je technologie použita v originální M16 a jejich variant odvozených Eugene Stonerem v 50. letech. Plyn, který pohání kulku protékal přes malou dírku v hlavní. Tento plyn byl malou trubičkou veden přímo k závěru, kde jej plyn zatlačil dozadu, aby závěr vyhodil použitou nábojnici, zároveň probíhalo stlačování pružiny, která vrátila závěr do původní pozice. Při návratu závěru byl nabit nový náboj do komory.</p>
				<p>Všechny lower receivery fungují stejně. Mají mechanismus střelby, který je složen ze spouště, pojistky, uvolnění zásobníku a uvolnění kladívka. V případě M16 je pojistka také volič režimu palby. Funkčně je mechanismus střelby složen ze spouště, pružin, kladiva, které jsou drženy několika kolíky. Závorník, pružina zpětného rázu a trubice na závorník je také součástí lower receiveru.</p>
				<p>Lower receiver by měl mít označení s ráží munice, na kterou je určen. Někdy může být označen jako multi. Například lower pro 5.56 bude fungovat s .300 AAC, ale ne s .308.</p>
				<p>Zásobník je také součástí lower receiveru. Zásobníky pro AR-15 mohou být kovové, plastové nebo z kompozitu. Jejich kapacita může držet cokoliv od 5 ran až po 100. Většinou je kapacita 30 ran.</p>
				<p>AR-15 existuje v mnoha rážích. Vše mezi .22 LR až .50 BMG. Nejčastější je .223/5.56 x 45. Mnoho lidí si myslí, že tyto dva náboje jsou identické, ale není tomu tak. Náboj 5.56 se vyrábí jinak a existuje dost rozdílu na to aby puška na ráží .223 nemohla bezpečně vystřelit 5.56. Opačně ale problém není. Mezi další možné ráže patří: </p>
