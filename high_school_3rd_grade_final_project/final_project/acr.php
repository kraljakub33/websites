				<h2>Co to je ACR?</h2>
				<p>Adaptive Combat Rifle (ACR) je útočná puška navržená Magpul Industries pod názvem Masada Adaptive Combat System (přizpůsobivý bojový zbraňový systém Masada)</p>
				<p>Útočná puška ACR funguje na systému rotačního závěrníku, používá náboje 5,56x45mm NATO, což v civilním případě je .223 Remington a po výměně úderníku, hlavně a zásobníkové šachty je dále schopna střílet ráži 7,62x39mm. Zásobníková šachta je široce kompatibilní se škálou zásobníků zbraní typu AK nebo náboje 6,8x43mm Remington SPC.</p>
				<h2>Historie</h2>
				<p>ACR jako nová modulární útočná puška pro vojenské a policejní účely, stejně jako pro civilisty byla vyvíjena zároveň s programem na vývoj útočné pušky SCAR (2006). Projekt byl zcela nezávislý na státní financování a Masada měla podle plánů nahradit útočnou pušku M16.</p>
				<p>První prototyp Magpul Masady byl představen veřejnosti na výstavě SHOT Show v roce 2007 v Orlandu na Floridě. S příchodem roku 2008 přišlo ohlášení o masové produkci nové útočné pušky a k projektu se připojila firma Bushmaster Firearms, ta nakonec získala oprávnění k výrobě, prodeji a dalšímu vývoji zbraně.</p>
				<p>V listopadu roku 2008 byla zbraň prezentována představitelům americké armády a o týden později vydal Bushmaster prohlášení, kde ohlásil přepracování konceptu zbraně Magpul Masada tak, aby se mohla zúčastnit soutěže pro americkou armádu, kde měla nahradit karabinu M4. Vznikl tak nový koncept s názvem ACR – Adaptive Combat Rifle.</p>
				<p>V současné době se ACR vyrábí ve dvou verzích a to v civilní a vojenské, základním rozdílem je to, že zbraň pro civilní účely je pouze poloautomatická a vyrábí ji dále Bushmaster Firearms pod názvem Bushmaster ACR, zatímco vojenská je plně automatická a pod patentem firmy Remington Arms pod název Remington ACR. Od poloviny roku 2010 je dostupná pro složky policejní, vojenskou a civilní.</p>
				<span class="image main"><img src="ACR_Range_3.jpg" alt="ACR DMR Range" /></span>
				<h2>Design</h2>
				<p>Design původní Magpul Masady představoval kombinaci několika rozdílných zbraní tak, aby designeři Magpulu mohli převzít to nejlepší a vytvořit tak jednotnou, lehkou a modulární pušku.</p>
				<p>Pro Masadu byl například ze série Armalite AR-18 převzat systém odebírání prachových částic plynů z hlavně. Ze série SCAR vnitřní tělo a závěr zbraně. Dále pak se stal inspirací Heckler & Koch G36 a XM8 svým rozsáhlým použitím lehkých polymerových součástek. Z M16/AR-15 zase přibyl přepínač střelby a hlaveň, a také systém spouště.</p>
				<p>Zbraň obsahuje i několik vylepšení speciálně od Magpulu jako například možnost rychlé výměny hlavně s nastavitelným plynovým regulátorem a nevratným závěrem. Verze od Magpulu má navíc ještě několik změn oproti Bushmasteru, nejzřejmější je posunutí ambidextrozního závěru přední polohy tak, jak je to vidět například u německých G3 a MP5.</p>
